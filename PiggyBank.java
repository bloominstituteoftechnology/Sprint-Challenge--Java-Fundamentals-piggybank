
public abstract class PiggyBank {

    private int total = 0;
    private double worth = 0;
    String name;

    public PiggyBank(){
        this.total = total;
    }

    public int getTotal() {
        return total;
    }

    public double getWorth() {
        return worth;
    }

    public String getName() {
        return name;
    }
}
